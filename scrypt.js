// Метод forEach - Позволяет перебирать последовательно все элементы массива
// Метод в параметре получает функцию, которая выполняется для каждого элемента массива
// в эту функцию МОЖНО передовать три параметра. так как они не обязательные
// если эти параметры есть то:
// в первый попадает элемент массива
// во второй попадает индекс элемента массива
// в третий сам массив

/*
Синтаксис:

массива.forEach(function(элемент, индекс, сам массив){
    код, который выполняется для всех элементов
})

*/

let arr = [100, 200, 800, 1000, 600];
arr.forEach((el, index, mas) => {
    let newEl = el / 2;
    mas.push(newEl);
})

console.log(arr);

