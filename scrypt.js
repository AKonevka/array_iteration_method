// Метод map - применяет заданную функцию для каждого элемента массива
// Метод не изменяет исходный массив, а возвращает изменённый.


/*
Синтаксис:

let новый массив = массив.map(function(элемент, индекс, сам массив){
    код, который выполняется для всех элементов
    return изменённый массив;
})

*/

let arrMap = [100, 200, 300, 1000, 800, 600];


let res1 = arrMap.forEach((el, index, mas) => {
    return el = el / 2;
})

console.log('forEach ', res1);



let res2 = arrMap.map((el, index, mas) => {
    return el = el / 2;
})

console.log('map ', res2);





